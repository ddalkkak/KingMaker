export function Cup({ isOpen = false, content = null, isHighlighted = false, size = 80 }) {
  const cupFill  = isHighlighted ? "#ffcc00" : "#884400";
  const cupLight = isHighlighted ? "#ffee44" : "#aa6600";
  const W2 = 16, H2 = 22;
  return (
    <svg width={size} height={Math.round(size * H2 / W2)}
      viewBox={`0 0 ${W2*5} ${H2*5}`}
      style={{ imageRendering:"pixelated", shapeRendering:"crispEdges" }}>
      {!isOpen ? (<>
        {R5(2,3,12,13,C.K,"cb")} {R5(3,4,10,11,cupFill,"cf")}
        {R5(1,2,14,2,C.K,"ct")} {R5(2,2,12,2,cupLight,"ctf")}
        {R5(1,15,14,2,C.K,"cbt")} {R5(2,15,12,2,cupFill,"cbf")}
        {R5(13,6,3,4,C.K,"ch")} {R5(14,7,2,2,cupFill,"chf")}
        {/* 삐져나온 */}
        {R5(1,5,1,1,cupFill,"cx1")} {R5(14,16,1,1,C.K,"cx2")}
        {isHighlighted && <>{R5(4,5,2,8,"#ffee88","hl")}{R5(3,4,1,1,"#ffee88","hlx")}</>}
      </>) : (<>
        {R5(2,0,12,12,C.K,"cb")} {R5(3,1,10,10,cupFill,"cf")}
        {R5(1,11,14,2,C.K,"cbt")} {R5(2,12,12,2,cupLight,"ctf")}
        {R5(0,6,1,1,cupFill,"cx")}{/* 삐져나온 */}
        {content === "loyal"   && <g transform={`translate(${2*5},${14*5})`}><LoyalMinister   size={size*0.55}/></g>}
        {content === "traitor" && <g transform={`translate(${2*5},${14*5})`}><TraitorMinister size={size*0.55}/></g>}
      </>)}
    </svg>
  );
}
