// 5px 그리드 rect 헬퍼
function R5(col, row, w = 1, h = 1, color, key) {
  if (!color) return null;
  return (
    <rect
      key={key ?? `r${col}-${row}-${color}`}
      x={col * 5} y={row * 5}
      width={w * 5} height={h * 5}
      fill={color}
      shapeRendering="crispEdges"
    />
  );
}

